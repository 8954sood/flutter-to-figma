Future<void> main(List<String> args) async {
  final projectPath = Directory.current.path;
  _log('========================================');
  _log(' Flutter вҶ’ Figma Layout Exporter');
  _log('========================================');
  _log('[Project] $projectPath');

  // ---- VM Service URI ----
  String? vmUri;
  if (args.isNotEmpty) {
    vmUri = args[0];
    _log('[URI] мҲҳлҸҷ м§Җм •: $vmUri');
  } else {
    vmUri = await discoverVmServiceUri();
  }
  if (vmUri == null) {
    _log('');
    _log('[ERROR] мӢӨн–ү мӨ‘мқё Flutter м•ұмқ„ м°ҫмқ„ мҲҳ м—ҶмҠөлӢҲлӢӨ.');
    _log('');
    _log('н•ҙкІ° л°©лІ•:');
    _log('  1) Flutter м•ұмқ„ л””лІ„к·ё лӘЁл“ңлЎң лЁјм Җ мӢӨн–үн•ҳм„ёмҡ”.');
    _log('  2) мҪҳмҶ”м—җ м¶ңл Ҙлҗң VM Service URIлҘј мқёмһҗлЎң м „лӢ¬н•ҳм„ёмҡ”:');
    _log('     dart tools/export_figma_layout.dart ws://127.0.0.1:PORT/TOKEN=/ws');
    exit(1);
  }

  // ---- нҒ¬лЎӨлҹ¬ мЈјмһ… ----
  final needsReload = injectCrawler(projectPath);

  // ---- WebSocket м—°кІ° ----
  _log('[Connect] $vmUri');
  late WebSocket ws;
  try {
    ws = await WebSocket.connect(vmUri);
  } catch (e) {
    _log('[ERROR] WebSocket м—°кІ° мӢӨнҢЁ: $e');
    _log('URIк°Җ мҳ¬л°”лҘём§Җ, Flutter м•ұмқҙ л””лІ„к·ё лӘЁл“ңлЎң мӢӨн–ү мӨ‘мқём§Җ нҷ•мқён•ҳм„ёмҡ”.');
    exit(1);
  }
  ws.listen(_onWsData, onError: (e) => _log('[WS ERROR] $e'));

  try {
    // ---- getVM вҶ’ isolateId ----
    final vm = await rpcCall(ws, 'getVM') as Map<String, dynamic>;
    final isolates =
        (vm['isolates'] as List).cast<Map<String, dynamic>>();
    if (isolates.isEmpty) throw Exception('VMм—җ isolateк°Җ м—ҶмҠөлӢҲлӢӨ.');
    final isolateId = isolates[0]['id'] as String;
    _log('[VM] isolate: $isolateId');

    // ---- Hot Reload (н•„мҡ” мӢң) ----
    if (needsReload) {
      _log('[HotReload] reloadSources нҳём¶ң мӨ‘...');
      await rpcCall(ws, 'reloadSources', {'isolateId': isolateId});
      await Future.delayed(const Duration(seconds: 3));
      _log('[HotReload] мҷ„лЈҢ');
    }

    // ---- нҒ¬лЎӨлҹ¬ лқјмқҙлёҢлҹ¬лҰ¬ м°ҫкё° ----
    final iso = await rpcCall(ws, 'getIsolate', {'isolateId': isolateId})
        as Map<String, dynamic>;
    final libs =
        (iso['libraries'] as List).cast<Map<String, dynamic>>();
    final crawlerLib = libs.cast<Map<String, dynamic>?>().firstWhere(
          (l) => (l!['uri'] as String).contains('figma_temp_crawler'),
          orElse: () => null,
        );

    await Future.delayed(const Duration(milliseconds: 1500));

    // ---- evaluate (мқҙлҜём§Җ нҸ¬н•Ё async export мӢңлҸ„) ----
    _log('[Evaluate] figmaStartExportWithImages() нҳём¶ң...');
    dynamic result;
    if (crawlerLib != null) {
      try {
        // Phase 1: async export мӢңмһ‘
        await rpcCall(ws, 'evaluate', {
          'isolateId': isolateId,
          'targetId': crawlerLib['id'],
          'expression': 'figmaStartExportWithImages()',
        });

        // Phase 2: кІ°кіј нҸҙл§Ғ (мөңлҢҖ 30мҙҲ)
        for (int i = 0; i < 60; i++) {
          await Future.delayed(const Duration(milliseconds: 500));
          final pollResult = await rpcCall(ws, 'evaluate', {
            'isolateId': isolateId,
            'targetId': crawlerLib['id'],
            'expression': 'figmaGetExportResult()',
          }) as Map<String, dynamic>;
          if (pollResult['valueAsString'] != null &&
              pollResult['valueAsString'] != 'null') {
            result = pollResult;
            _log('[Evaluate] async export мҷ„лЈҢ (${i * 500}ms)');
            break;
          }
        }

        // нҸҙл§Ғ нғҖмһ„м•„мӣғ мӢң sync fallback
        if (result == null) {
          _log('[Evaluate] async export нғҖмһ„м•„мӣғ вҶ’ sync fallback');
          result = await rpcCall(ws, 'evaluate', {
            'isolateId': isolateId,
            'targetId': crawlerLib['id'],
            'expression': 'figmaExtractorEntryPoint()',
          });
        }
      } catch (_) {
        _log('[Evaluate] лқјмқҙлёҢлҹ¬лҰ¬ м»Ён…ҚмҠӨнҠё мӢӨнҢЁ вҶ’ м „м—ӯ м»Ён…ҚмҠӨнҠё мһ¬мӢңлҸ„');
        result = await rpcCall(ws, 'evaluate', {
          'isolateId': isolateId,
          'expression': 'figmaExtractorEntryPoint()',
        });
      }
    } else {
      result = await rpcCall(ws, 'evaluate', {
        'isolateId': isolateId,
        'expression': 'figmaExtractorEntryPoint()',
      });
    }

    // ---- кІ°кіј м¶”м¶ң (truncation мІҳлҰ¬) ----
    final res = result as Map<String, dynamic>;
    String jsonString;
    if (res['valueAsString'] != null &&
        res['valueAsStringIsTruncated'] != true) {
      jsonString = res['valueAsString'] as String;
    } else if (res['id'] != null) {
      _log('[Evaluate] кІ°кіјк°Җ мһҳлҰј вҶ’ getObjectлЎң м „мІҙ к°Җм ёмҳӨкё°...');
      final full = await rpcCall(ws, 'getObject', {
        'isolateId': isolateId,
        'objectId': res['id'],
      }) as Map<String, dynamic>;
      jsonString = full['valueAsString'] as String;
    } else {
      throw Exception('мҳҲмғҒм№ҳ лӘ»н•ң кІ°кіј: ${jsonEncode(result)}');
    }

    // ---- м ҖмһҘ ----
    final dumpDir = Directory('$projectPath/flutter_figma_dump');
    if (!dumpDir.existsSync()) dumpDir.createSync(recursive: true);

    File('${dumpDir.path}/figma_layout_raw.json')
        .writeAsStringSync(jsonString);

    final data = jsonDecode(jsonString);
    final pretty = const JsonEncoder.withIndent('  ').convert(data);
    File('${dumpDir.path}/figma_layout.json').writeAsStringSync(pretty);

    // нҒҙлҰҪліҙл“ң (macOS)
    try {
      final pb = await Process.start('pbcopy', []);
      pb.stdin.write(pretty);
      await pb.stdin.close();
      await pb.exitCode;
      _log('[Done] нҒҙлҰҪліҙл“ңм—җ ліөмӮ¬ мҷ„лЈҢ');
    } catch (_) {}

    _log('[Done] м ҖмһҘ вҶ’ flutter_figma_dump/figma_layout.json');
    _log('[Done] JSON кёёмқҙ: ${jsonString.length} chars');
    _log('========================================');
  } finally {
    await ws.close();
  }
}
