/// RenderShaderMask의 public shaderCallback getter를 dynamic dispatch로 접근,
/// 사각형에 shader를 직접 칠한 뒤 픽셀 샘플링으로 gradient 추출
Future<void> _extractShaderMaskGradients(RenderObject root) async {
  final List<RenderBox> nodes = [];
  void find(RenderObject ro) {
    final name = ro.runtimeType.toString();
    if (name.contains('ShaderMask') && ro is RenderBox && ro.hasSize) {
      nodes.add(ro);
    }
    ro.visitChildren(find);
  }
  find(root);
  debugPrint('[ShaderMask] found ${nodes.length} nodes');

  for (final node in nodes) {
    try {
      final w = node.size.width;
      final h = node.size.height;
      if (w <= 0 || h <= 0) continue;

      // dynamic dispatch로 shaderCallback 접근
      final dynamic dynNode = node;
      final Function callback = dynNode.shaderCallback as Function;
      final rect = Offset.zero & Size(w, h);
      final shader = callback(rect) as ui.Shader;
      debugPrint('[ShaderMask] got shader from callback, size=${w}x${h}');

      // 사각형 전체를 shader로 칠해서 캡처 (텍스트 형태가 아닌 full rect)
      final sw = w.ceil().clamp(4, 64);
      final sh = h.ceil().clamp(4, 64);
      final recorder = ui.PictureRecorder();
      final canvas = Canvas(recorder);
      canvas.drawRect(
        Rect.fromLTWH(0, 0, sw.toDouble(), sh.toDouble()),
        Paint()..shader = shader,
      );
      final picture = recorder.endRecording();
      final image = await picture.toImage(sw, sh);
      final byteData = await image.toByteData(format: ui.ImageByteFormat.rawRgba);
      image.dispose();
      if (byteData == null) {
        debugPrint('[ShaderMask] byteData null');
        continue;
      }

      Color getPixel(int x, int y) {
        x = x.clamp(0, sw - 1);
        y = y.clamp(0, sh - 1);
        final off = (y * sw + x) * 4;
        return Color.fromARGB(
          byteData.getUint8(off + 3),
          byteData.getUint8(off),
          byteData.getUint8(off + 1),
          byteData.getUint8(off + 2),
        );
      }

      double colorDist(Color a, Color b) =>
          ((a.red - b.red).abs() + (a.green - b.green).abs() + (a.blue - b.blue).abs()).toDouble();

      // 방향 판별
      const n = 6;
      double hVar = 0, vVar = 0;
      for (int i = 1; i < n; i++) {
        hVar += colorDist(
          getPixel(((i - 1) * (sw - 1) ~/ (n - 1)), sh ~/ 2),
          getPixel((i * (sw - 1) ~/ (n - 1)), sh ~/ 2),
        );
        vVar += colorDist(
          getPixel(sw ~/ 2, ((i - 1) * (sh - 1) ~/ (n - 1))),
          getPixel(sw ~/ 2, (i * (sh - 1) ~/ (n - 1))),
        );
      }
      final bool isVert = vVar >= hVar;
      debugPrint('[ShaderMask] hVar=$hVar vVar=$vVar isVert=$isVert');

      // 색상 샘플링
      const sc = 10;
      final colors = <String>[];
      final stops = <double>[];
      for (int i = 0; i < sc; i++) {
        final t = i / (sc - 1);
        final c = isVert
            ? getPixel(sw ~/ 2, (t * (sh - 1)).round())
            : getPixel((t * (sw - 1)).round(), sh ~/ 2);
        colors.add(_colorToHex(c)!);
        stops.add(t);
      }

      // 간소화: 연속 중복 제거
      final sC = <String>[colors.first];
      final sS = <double>[stops.first];
      for (int i = 1; i < colors.length - 1; i++) {
        if (colors[i] != colors[i - 1] || colors[i] != colors[i + 1]) {
          sC.add(colors[i]);
          sS.add(stops[i]);
        }
      }
      sC.add(colors.last);
      sS.add(stops.last);

      final gradient = <String, dynamic>{
        'type': 'linear',
        'colors': sC,
        'stops': sS,
        'begin': {'x': isVert ? 0.5 : 0.0, 'y': isVert ? 0.0 : 0.5},
        'end': {'x': isVert ? 0.5 : 1.0, 'y': isVert ? 1.0 : 0.5},
      };
      debugPrint('[ShaderMask] gradient colors: $sC');

      void mapAll(RenderObject obj) {
        _shaderMaskGradients[obj] = gradient;
        obj.visitChildren(mapAll);
      }
      node.visitChildren(mapAll);
    } catch (e, st) {
      debugPrint('[ShaderMask] ERROR: $e\n$st');
    }
  }
}

/// =======================================================
/// 0.5 Pre-capture: 이미지/아이콘 → base64 (async)
/// =======================================================

Future<void> _preCaptureImages(RenderObject node) async {
  // RenderImage → 축소 후 PNG 인코딩 (최대 512px)
  if (node is RenderImage) {
    try {
      final ui.Image? img = node.image;
      if (img != null) {
        final maxDim = 512;
        int targetW = img.width;
        int targetH = img.height;
        if (targetW > maxDim || targetH > maxDim) {
          final scale = maxDim / (targetW > targetH ? targetW : targetH);
          targetW = (targetW * scale).ceil();
          targetH = (targetH * scale).ceil();
        }
        final recorder = ui.PictureRecorder();
        final canvas = Canvas(recorder);
        canvas.drawImageRect(
          img,
          Rect.fromLTWH(0, 0, img.width.toDouble(), img.height.toDouble()),
          Rect.fromLTWH(0, 0, targetW.toDouble(), targetH.toDouble()),
          Paint(),
        );
        final picture = recorder.endRecording();
        final resized = await picture.toImage(targetW, targetH);
        final byteData = await resized.toByteData(
          format: ui.ImageByteFormat.png,
        );
        if (byteData != null) {
          _imageDataByNode[node] = base64Encode(byteData.buffer.asUint8List());
        }
      }
    } catch (_) {}
  }

  // MaterialIcons → TextPainter → PictureRecorder → toImage()
  if (node is RenderParagraph) {
    try {
      final span = node.text;
      if (span is TextSpan &&
          span.style?.fontFamily?.contains('MaterialIcons') == true) {
        final recorder = ui.PictureRecorder();
        final canvas = Canvas(
          recorder,
          Rect.fromLTWH(0, 0, node.size.width, node.size.height),
        );
        final painter = TextPainter(
          text: span,
          textDirection: TextDirection.ltr,
        );
        painter.layout();
        painter.paint(canvas, Offset.zero);
        final picture = recorder.endRecording();
        final img = await picture.toImage(
          node.size.width.ceil(),
          node.size.height.ceil(),
        );
        final byteData = await img.toByteData(format: ui.ImageByteFormat.png);
        if (byteData != null) {
          _imageDataByNode[node] = base64Encode(byteData.buffer.asUint8List());
        }
      }
    } catch (_) {}
  }

  // CustomPaint with painter → 투명 배경에 painter만 직접 paint
  if (node is RenderBox &&
      _customPainterByRO.containsKey(node) &&
      !_imageDataByNode.containsKey(node)) {
    try {
      final painter = _customPainterByRO[node]!;
      final w = node.size.width;
      final h = node.size.height;
      if (w > 0 && h > 0) {
        const double pr = 2.0;
        final outW = (w * pr).round();
        final outH = (h * pr).round();
        final recorder = ui.PictureRecorder();
        final canvas = Canvas(recorder, Rect.fromLTWH(0, 0, outW.toDouble(), outH.toDouble()));
        canvas.scale(pr);
        painter.paint(canvas, Size(w, h));
        final picture = recorder.endRecording();
        final img = await picture.toImage(outW, outH);
        final byteData = await img.toByteData(format: ui.ImageByteFormat.png);
        img.dispose();
        if (byteData != null) {
          _imageDataByNode[node] = base64Encode(byteData.buffer.asUint8List());
        }
      }
    } catch (e) {
      debugPrint('[CustomPainter] direct paint failed: $e');
    }
  }

  // Checkbox / Switch 등 → 가장 가까운 RepaintBoundary에서 crop 캡처
  // 실제 컴포넌트보다 사방 2px 넓게 캡처하여 overflow paint(리플/그림자) 포함
  // 출력 PNG는 항상 (w+4)x(h+4) 크기, 소스 부족 영역은 투명
  if (node is RenderBox &&
      _customPaintCaptures.contains(node) &&
      !_imageDataByNode.containsKey(node)) {
    try {
      final w = node.size.width;
      final h = node.size.height;
      if (w > 0 && h > 0) {
        const double pr = 2.0;
        const double pad = 4.0;
        // 충분히 큰 RepaintBoundary를 찾음 (노드보다 사방 pad 이상 큰 것)
        RenderObject? ancestor = node.parent;
        RenderRepaintBoundary? bestBoundary;
        while (ancestor != null) {
          if (ancestor is RenderRepaintBoundary) {
            bestBoundary = ancestor;
            if (ancestor.size.width >= w + pad * 2 &&
                ancestor.size.height >= h + pad * 2) {
              break;
            }
          }
          ancestor = ancestor.parent;
        }
        ancestor = bestBoundary;
        if (ancestor is RenderRepaintBoundary) {
          final img = await ancestor.toImage(pixelRatio: pr);
          final nodeOffset = node.localToGlobal(Offset.zero);
          final boundaryOffset = (ancestor as RenderBox).localToGlobal(
            Offset.zero,
          );
          // 출력 이미지 크기 (항상 고정)
          final outW = ((w + pad * 2) * pr).round();
          final outH = ((h + pad * 2) * pr).round();
          // 소스 crop 영역 (pixel 좌표)
          final rawSrcX = (nodeOffset.dx - boundaryOffset.dx - pad) * pr;
          final rawSrcY = (nodeOffset.dy - boundaryOffset.dy - pad) * pr;
          // 클램프: 소스 이미지 밖이면 잘라내고, 대상 오프셋 조정
          final srcX = rawSrcX.clamp(0.0, img.width.toDouble());
          final srcY = rawSrcY.clamp(0.0, img.height.toDouble());
          final dstX = srcX - rawSrcX; // 소스가 잘린만큼 대상에서 오프셋
          final dstY = srcY - rawSrcY;
          final srcW = (outW.toDouble() - dstX).clamp(
            0.0,
            img.width.toDouble() - srcX,
          );
          final srcH = (outH.toDouble() - dstY).clamp(
            0.0,
            img.height.toDouble() - srcY,
          );
          if (srcW > 0 && srcH > 0) {
            final recorder = ui.PictureRecorder();
            final canvas = Canvas(
              recorder,
              Rect.fromLTWH(0, 0, outW.toDouble(), outH.toDouble()),
            );
            // 배경 투명 (기본값)
            canvas.drawImageRect(
              img,
              Rect.fromLTWH(srcX, srcY, srcW, srcH),
              Rect.fromLTWH(dstX, dstY, srcW, srcH),
              Paint(),
            );
            final picture = recorder.endRecording();
            final cropped = await picture.toImage(outW, outH);
            final byteData = await cropped.toByteData(
              format: ui.ImageByteFormat.png,
            );
            if (byteData != null) {
              _imageDataByNode[node] = base64Encode(
                byteData.buffer.asUint8List(),
              );
            }
          }
        }
      }
    } catch (_) {}
  }

  // 자식 재귀 (visitChildren은 sync callback → 리스트 수집 후 순회)
  final children = <RenderObject>[];
  node.visitChildren((child) => children.add(child));
  for (final child in children) {
    await _preCaptureImages(child);
  }
}

/// =======================================================
/// 1. Element 트리 순회해서 위젯 레벨 디자인 정보 수집
/// =======================================================

void _collectDesignInfoFromElements(Element element) {
  final widget = element.widget;

  if (widget is InputDecorator) {
    final InputDecoration deco = widget.decoration;
    Color? bg;
    Color? borderColor;
    double? borderWidth;
    double? radius;

    if (deco.filled == true) {
      bg = deco.fillColor;
    }

    final InputBorder? border =
        deco.enabledBorder ?? deco.focusedBorder ?? deco.border;

    if (border is OutlineInputBorder) {
      final side = border.borderSide;
      borderColor = side.color;
      borderWidth = side.width;
      final br = border.borderRadius;
      if (br is BorderRadius) {
        radius = br.topLeft.x;
      }
    }

    final ro = element.renderObject;
    if (ro != null && (bg != null || borderColor != null || radius != null)) {
      _designInfoByRenderObject[ro] = DesignInfo(
        backgroundColor: bg,
        borderColor: borderColor,
        borderWidth: borderWidth,
        borderRadius: radius,
        isTextField: true,
      );
    }
  }

  if (widget is Divider) {
    final ro = element.renderObject;
    if (ro != null && widget.color != null) {
      _designInfoByRenderObject[ro] = DesignInfo(
        backgroundColor: widget.color,
        isDivider: true,
      );
    }
  }

  if (widget is Container) {
    final deco = widget.decoration;
    Color? bg;
    Color? borderColor;
    double? borderWidth;
    double? radius;
    double? borderTopW, borderRightW, borderBottomW, borderLeftW;

    if (deco is BoxDecoration) {
      bg = deco.color;
      final border = deco.border;
      if (border is Border) {
        final sides = [border.top, border.right, border.bottom, border.left];
        BorderSide? best;
        for (final s in sides) {
          if (best == null || s.width > best.width) best = s;
        }
        if (best != null && best.width > 0) {
          borderColor = best.color;
          borderWidth = best.width;
        }
        final uniform = sides.every(
          (s) => s.width == sides[0].width && s.color == sides[0].color,
        );
        if (!uniform) {
          borderTopW = border.top.width;
          borderRightW = border.right.width;
          borderBottomW = border.bottom.width;
          borderLeftW = border.left.width;
        }
      }
      final br = deco.borderRadius;
      if (br is BorderRadius) {
        radius = br.topLeft.x;
      }
    }

    final ro = element.renderObject;
    if (ro != null && (bg != null || borderColor != null)) {
      _designInfoByRenderObject[ro] = DesignInfo(
        backgroundColor: bg,
        borderColor: borderColor,
        borderWidth: borderWidth,
        borderRadius: radius,
        borderTopWidth: borderTopW,
        borderRightWidth: borderRightW,
        borderBottomWidth: borderBottomW,
        borderLeftWidth: borderLeftW,
      );
    }
  }

  if (widget.runtimeType.toString().contains('SvgPicture')) {
    final ro = element.renderObject;
    if (ro != null) {
      _svgBoxTargets.add(ro);
    }
  }

  // Material 위젯 (OutlinedButton, ElevatedButton 등의 보더)
  // _ShapeBorderPaint(CustomPaint)로 그려지는 보더는 render tree에서 추출 불가
  // → element tree에서 Material.shape.side를 직접 추출
  if (widget is Material && widget.shape != null) {
    final shape = widget.shape;
    if (shape is OutlinedBorder) {
      final side = shape.side;
      if (side.width > 0 && side.style == BorderStyle.solid) {
        double? radius;
        if (shape is RoundedRectangleBorder) {
          final br = shape.borderRadius;
          if (br is BorderRadius) {
            radius = br.topLeft.x;
          }
        }
        final ro = element.renderObject;
        if (ro != null) {
          // 기존 DesignInfo가 있으면 덮어쓰지 않고, 없을 때만 등록
          if (!_designInfoByRenderObject.containsKey(ro)) {
            _designInfoByRenderObject[ro] = DesignInfo(
              borderColor: side.color,
              borderWidth: side.width,
              borderRadius: radius,
            );
          }
        }
      }
    }
  }

  // 전용 위젯 이름 태깅
  final widgetTypeName = widget.runtimeType.toString();
  const namedWidgets = {'NavigationToolbar', 'BottomNavigationBar', 'Scaffold'};
  if (namedWidgets.contains(widgetTypeName)) {
    final ro = element.renderObject;
    if (ro != null) {
      _widgetNameByRenderObject[ro] = widgetTypeName;
    }
  }

  // Checkbox / Switch → renderObject와 하위 자식 모두 캡처 대상 등록
  const captureWidgets = {
    'Checkbox', 'Switch', 'CupertinoSwitch',
    'Slider', 'CircularProgressIndicator', 'LinearProgressIndicator',
    'ChoiceChip', 'FilterChip', 'InputChip', 'ActionChip',
  };
  if (captureWidgets.contains(widgetTypeName)) {
    final ro = element.renderObject;
    if (ro != null) {
      _markCaptureRecursive(ro);
    }
  }

  // ShaderMask → render tree 순회로 감지 (async phase에서 처리)

  // CustomPaint with painter → painter를 직접 빈 캔버스에 paint (투명 배경)
  // child가 없는 leaf CustomPaint만 캡처 (순수 장식용)
  // Flutter 내부 CustomPaint(child 있음: ink effect, indicator 등)는 스킵 → 자연 분해
  if (widgetTypeName == 'CustomPaint') {
    final cp = widget as CustomPaint;
    if (cp.painter != null && cp.child == null) {
      final ro = element.renderObject;
      if (ro != null) {
        _customPainterByRO[ro] = cp.painter!;
        _markCaptureRecursive(ro);
      }
    }
  }

  // BackdropFilter → blur sigma 추출
  if (widget is BackdropFilter) {
    try {
      final filter = widget.filter;
      final filterStr = filter.toString();
      // ImageFilter.blur(sigmaX: 10.0, sigmaY: 10.0, ...)
      final sigmaMatch = RegExp(r'sigmaX:\s*([\d.]+)').firstMatch(filterStr);
      if (sigmaMatch != null) {
        final sigma = double.tryParse(sigmaMatch.group(1)!) ?? 0;
        if (sigma > 0) {
          final ro = element.renderObject;
          if (ro != null) {
            _blurInfoByRenderObject[ro] = sigma;
          }
        }
      }
    } catch (_) {}
  }

  // Transform.rotate → angle 추출
  if (widget is Transform) {
    try {
      final ro = element.renderObject;
      if (ro != null) {
        // Transform widget의 transform Matrix4에서 rotation 추출
        final t = widget.transform;
        final sinVal = t.entry(1, 0);
        final cosVal = t.entry(0, 0);
        final radians = math.atan2(sinVal, cosVal);
        if (radians.abs() > 0.001) {
          final degrees = radians * 180.0 / math.pi;
          _rotationByRenderObject[ro] = degrees;
          debugPrint('[Transform] captured rotation=${degrees.toStringAsFixed(2)}° for $ro');
        }
      }
    } catch (e) {
      debugPrint('[Transform] angle extraction failed: $e');
    }
  }

  element.visitChildren(_collectDesignInfoFromElements);
}

